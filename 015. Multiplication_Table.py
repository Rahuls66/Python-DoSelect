#15. Multiplication Table

T = int(input())
l1 = []
for _ in range(T):
    l1.append(int(input()))
for num in l1:
    for j in range(1,11):    
        print(str(num) + " x " + str(j) + " = " + str(num*j))
        j += 1
    print("===============")
    
